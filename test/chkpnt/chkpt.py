#!/usr/bin/python3

import argparse
import json
import sys

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("json_file", type=str, help="schema json file generated by SST")
    args = parser.parse_args()

    with open(args.json_file) as schema_file:
        schema = json.load(schema_file);

    print(schema)


if __name__ == "__main__":
    main()
    
